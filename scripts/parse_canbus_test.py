import re

def filter_id_data(data, target_id='0001'):
    """
    Filters a block of text, keeping only the lines where the fourth 
    whitespace-separated field (the ID) matches the target_id.

    Args:
        data (str): The raw input text containing multiple lines.
        target_id (str): The specific ID string to match (default is '0001').

    Returns:
        str: A string containing only the lines that matched the target_id.
    """
    
    # Split the raw data into individual lines
    lines = data.strip().split('\n')
    
    filtered_lines = []
    
    # Process each line
    for line in lines:
        # Use regex to split the line by one or more whitespace characters
        # This handles inconsistent spacing between columns.
        fields = re.split(r'\s+', line.strip())
        
        # Check if the line has enough fields (at least 4)
        if len(fields) >= 4:
            # The ID is in the 4th field (index 3, assuming 0-indexing)
            current_id = fields[3]
            
            # Check if the current ID matches the target ID
            if current_id == target_id:
                filtered_lines.append(line)
            # If the current ID is NOT the target ID, it is filtered out.
            
    # Join the filtered lines back into a single string
    return '\n'.join(filtered_lines)

