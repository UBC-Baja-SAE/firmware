import re
import struct
import matplotlib.pyplot as plt

def convert_and_extract_for_plot(data):
    """
    Parses a block of text, extracts the line index and the 8-byte hexadecimal 
    sequence from each line, converts the hex to a decimal (IEEE 754 64-bit, 
    Little-Endian), and returns lists suitable for plotting.

    This version is designed for pipe-delimited data (Index | Hex | Decimal).

    Args:
        data (str): The raw input text containing multiple lines of data.

    Returns:
        tuple: (list of line indices, list of calculated decimal values)
    """
    
    # NEW Regex pattern to capture the index and the hex data from the pipe-delimited format:
    # 1. Capture the index: ^\s*(\d+)\s*
    # 2. Match the first pipe: \|\s*
    # 3. Capture the hex bytes (everything up to the next pipe): ([^|]+)
    line_pattern = re.compile(r'^\s*(\d+)\s*\|\s*([^|]+?)\s*\|', re.MULTILINE | re.IGNORECASE)
    
    indices = []
    decimal_values = []
    
    for match in line_pattern.finditer(data):
        try:
            # Group 1: The line index (e.g., '2', '6')
            line_index = int(match.group(1))
            
            # Group 2: The space-separated hex bytes (e.g., 'D4 1A DC 88 4A 7C 41 40')
            hex_data_spaced = match.group(2).strip()
            
            # Concatenate the hex bytes into a single 16-character string
            hex_data_raw = hex_data_spaced.replace(' ', '')
            
            # Convert the hex string to a binary bytes object
            byte_data = bytes.fromhex(hex_data_raw)
            
            # Unpack the bytes as a little-endian ('<') double-precision float ('d')
            # This is standard for CAN bus logs and similar data structures.
            decimal_value = struct.unpack('<d', byte_data)[0]
            
            indices.append(line_index)
            decimal_values.append(decimal_value)
            
        except Exception as e:
            # Print any lines that fail conversion (e.g., empty lines, formatting errors)
            print(f"Skipping line starting with index {match.group(1)} due to error: {e}")
                
    return indices, decimal_values

